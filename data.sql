  CREATE OR REPLACE FORCE VIEW "V_REVIEW_TO_RST" ("REVIEW_NUMBER", "RST_ID", "RATING") AS 
  (
        SELECT DISTINCT RV.REVIEW_NUMBER, RST.RST_ID, RV.RATING
        FROM REVIEW RV, ORDER_HISTORY OH, ORDER_DETAIL OD, MENU, MENU_CATEGORY MC, RESTAURANT RST
        WHERE RV.ORDER_NUMBER(+)=OH.ORDER_NUMBER AND OH.ORDER_NUMBER(+)=OD.ORDER_NUMBER AND OD.MENU_ID(+)=MENU.MENU_ID AND MENU.CATEGORY_ID(+)=MC.CATEGORY_ID AND MC.RST_ID(+)=RST.RST_ID
    )
;
