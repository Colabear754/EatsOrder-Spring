--  DDL for View V_RST_RATING
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "V_RST_RATING" ("RST_ID", "AVG_RATING") AS 
  (
        SELECT RST_ID, NVL(ROUND(AVG(RATING), 1), 0) AVG_RATING 
        FROM V_REVIEW_TO_RST 
        GROUP BY RST_ID
    )
;
