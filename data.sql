    FUNCTION ENCRYPT (input_string IN VARCHAR2 , key_data IN VARCHAR2 := '43668278110094530951087941128188')
     RETURN RAW
    IS
        key_data_raw        RAW(4000);
        converted_raw       RAW(4000);
